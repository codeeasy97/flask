# Python Math

#Built-in Math Functions
x = min(1,2,3,4)
print(x)

x = max(1,2,3,4)
print(x)

x = abs(-9.73)
print(x)

x = pow(2,3)
print(x)
#The Math Module

import math


x = math.ceil(1.67)
print(x)

x = math.floor(1.67)
print(x)

x = math.sqrt(64)
print(x)

x = math.pi
print(x)

x = math.tan(45)
print(x)

x = math.cos(90)
print(x)

x = math.sin(30)
print(x)